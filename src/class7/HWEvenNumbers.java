package class7;

public class HWEvenNumbers {

    public static void main(String[] args) {

        int i=20;
        while(i>=1)
        {
            System.out.print(i+" ");
            i-=2;
        }
        System.out.println();

        for (int j = 20; j >=1 ; j-=2) {
            System.out.print(j+" ");

        }
    }
}
