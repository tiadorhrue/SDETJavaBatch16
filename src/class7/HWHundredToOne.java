package class7;

public class HWHundredToOne {

    public static void main(String[] args) {

        int i=100;
        while(i>=1)
        {
            System.out.print(i+" ");
            i--;
        }
        System.out.println();

        for (int j = 100; j >=1 ; j--) {
            System.out.print(j+" ");
        }
        System.out.println();
        int k=100;
        do{
            System.out.print(k+" ");
            k--;
        }while(k>=1);
    }
}
