package class07;

public class HWOneLine {

    public static void main(String[] args) {

        int i=1;

        while(i<=100)
        {
            System.out.print(i+" ");
            i++;
        }

        System.out.println();
        for (int j = 1; j <=100; j++) {
            System.out.print(j+" ");
        }
        System.out.println();
        int k=1;
        do{
            System.out.print(k+" ");
            k++;
        }while(k<=100);
    }
}
