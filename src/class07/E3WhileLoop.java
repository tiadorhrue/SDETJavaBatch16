package class07;

public class E3WhileLoop {

    public static void main(String[] args) {
        // we want to write "Hello World" five times
        // in loops the code is tested in circles instead of top to bottom/left to right

        int counter=1;

        while(counter<=5){ // if the condition is not true the loop will not print
            counter++;
            System.out.println("Hello World");

            // it will continue to check and print the condition until it is no longer true



        }
    }
}
