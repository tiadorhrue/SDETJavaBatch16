package class08;

public class E14NestedLoops {

    public static void main(String[] args) {

        for (int i = 1; i < 3; i++) { // numbers showing in first row
            for (int j = 1; j < 4; j++) { // numbers printed in second row
                System.out.println(i+" "+j);
            }
        }

    }



}
