package class08;

public class E10NestedLoops {


    public static void main(String[] args) {

        int counter=0;
        counter++;
        while (counter<=4) {
            for (int i = 2; i <= 10; i += 2) {
                System.out.print(i + " ");

            }
            System.out.println();
        }


        }
    }

