package class3;

public class E5Modulus {

    public static void main(String[] args) {

        int number1=10;
        int number2=4;
        System.out.println(number1/number2);
        System.out.println(number1%number2); // modulus = remainder

        int number3=11;
        int number4=5;
        System.out.println(number3/number4);
        System.out.println(number3%number4); // modulus shows you what is left behind / pizza analogy

        // if the remainder is 0 the value is an even number if it's not the value is an odd number
    }
}
