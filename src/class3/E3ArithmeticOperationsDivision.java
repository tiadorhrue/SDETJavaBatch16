package class3;

public class E3ArithmeticOperationsDivision {

    public static void main(String[] args) {
        System.out.println(10/5);
        System.out.println(11/5); // Ran as an int so java will round down
        System.out.println(11.0/5.0); // if you want to see the remained you have to run it as a double

        int number1=10;
        int number2=4;
        System.out.println(number1/number2);

        double num1=10;
        double num2=4;  // data types also tells java which operation to perform
        System.out.println(num1/num2);
    }
}
