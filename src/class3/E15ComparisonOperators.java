package class3;

public class E15ComparisonOperators {

    public static void main(String[] args) {

        int num1=10;
        int num2=20;

        System.out.println(num1>num2);
        System.out.println(num1<num2);

        boolean result=num1>num2;
        System.out.println(result);

        System.out.println(num1==num2);
        System.out.println(num1!=num2);
        System.out.println(num1>=num2);
        System.out.println(num2<=num1);

    }
}
